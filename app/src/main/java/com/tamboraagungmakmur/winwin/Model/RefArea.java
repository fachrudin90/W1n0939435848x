public class RefArea {


    private String id;
    private String nama_area;
    private String rlc;


    public RefArea() {
    }


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama_area() {
        return nama_area;
    }

    public void setNama_area(String nama_area) {
        this.nama_area = nama_area;
    }

    public String getRlc() {
        return rlc;
    }

    public void setRlc(String rlc) {
        this.rlc = rlc;
    }
}
