public class LogActivityRekap {



    private String member_user;
    private String setujui;
    private String tolak;
    private String profil;
    private String telat;


    public LogActivityRekap() {
    }

    public String getMember_user() {
        return member_user;
    }

    public void setMember_user(String member_user) {
        this.member_user = member_user;
    }

    public String getSetujui() {
        return setujui;
    }

    public void setSetujui(String setujui) {
        this.setujui = setujui;
    }

    public String getTolak() {
        return tolak;
    }

    public void setTolak(String tolak) {
        this.tolak = tolak;
    }

    public String getProfil() {
        return profil;
    }

    public void setProfil(String profil) {
        this.profil = profil;
    }

    public String getTelat() {
        return telat;
    }

    public void setTelat(String telat) {
        this.telat = telat;
    }
}
