public class RefTahapPengajuan {


    private String id;
    private String ajutahap_label;
    private String ajutahap_code;
    private String ajutahap_penjelasan;
    private String stat_rlc;



    public RefTahapPengajuan() {
    }


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAjutahap_label() {
        return ajutahap_label;
    }

    public void setAjutahap_label(String ajutahap_label) {
        this.ajutahap_label = ajutahap_label;
    }

    public String getAjutahap_code() {
        return ajutahap_code;
    }

    public void setAjutahap_code(String ajutahap_code) {
        this.ajutahap_code = ajutahap_code;
    }

    public String getAjutahap_penjelasan() {
        return ajutahap_penjelasan;
    }

    public void setAjutahap_penjelasan(String ajutahap_penjelasan) {
        this.ajutahap_penjelasan = ajutahap_penjelasan;
    }

    public String getStat_rlc() {
        return stat_rlc;
    }

    public void setStat_rlc(String stat_rlc) {
        this.stat_rlc = stat_rlc;
    }
}
