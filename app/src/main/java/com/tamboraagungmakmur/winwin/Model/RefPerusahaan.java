public class RefPerusahaan {


    private String id;
    private String nama_perusahaan;
    private String alamat;
    private String kota;
    private String provinsi;
    private String telepon;
    private String jenis_komoditi;
    private String jenis_industri;
    private String stat_rlc;

    public RefPerusahaan() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama_perusahaan() {
        return nama_perusahaan;
    }

    public void setNama_perusahaan(String nama_perusahaan) {
        this.nama_perusahaan = nama_perusahaan;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getKota() {
        return kota;
    }

    public void setKota(String kota) {
        this.kota = kota;
    }

    public String getProvinsi() {
        return provinsi;
    }

    public void setProvinsi(String provinsi) {
        this.provinsi = provinsi;
    }

    public String getTelepon() {
        return telepon;
    }

    public void setTelepon(String telepon) {
        this.telepon = telepon;
    }

    public String getJenis_komoditi() {
        return jenis_komoditi;
    }

    public void setJenis_komoditi(String jenis_komoditi) {
        this.jenis_komoditi = jenis_komoditi;
    }

    public String getJenis_industri() {
        return jenis_industri;
    }

    public void setJenis_industri(String jenis_industri) {
        this.jenis_industri = jenis_industri;
    }

    public String getStat_rlc() {
        return stat_rlc;
    }

    public void setStat_rlc(String stat_rlc) {
        this.stat_rlc = stat_rlc;
    }
}
